package LabActivity_week2;

public class DefVal_primitiveDATAtype {
	int i;
	byte b;
	short s;
	long l;
	float f;
	double d;
	boolean boo;
	char ch;
	public static void main(String[] args) {
		DefVal_primitiveDATAtype A=new DefVal_primitiveDATAtype();
		System.out.println("default value of int is :  "+A.i);
		System.out.println("default value of byte is :  "+A.b);
		System.out.println("default value of short is :  "+A.s);
		System.out.println("default value of long is :  "+A.l);
		System.out.println("default value of float is :  "+A.f);
		System.out.println("default value of double is :  "+A.d);
		System.out.println("default value of boolean is :  "+A.boo);
		System.out.println("default value of character is :  "+A.ch);
	}
}
